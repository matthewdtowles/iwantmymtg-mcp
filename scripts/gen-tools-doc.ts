#!/usr/bin/env tsx
/**
 * Generate docs/TOOLS.md from the live tool registry so the documented tool
 * catalog can never drift from the code. CI runs this with `--check` and fails
 * if docs/TOOLS.md is out of date; run without a flag to regenerate it.
 *
 *   npm run gen:tools-doc        # write docs/TOOLS.md
 *   npm run check:tools-doc      # exit 1 if docs/TOOLS.md is stale
 *
 * Auth is derived from the description convention: every authenticated tool's
 * description ends with "Requires IWMM_API_KEY." (asserted in test/tools.test).
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { tools } from "../src/tools/index.js";

const OUT = resolve(dirname(fileURLToPath(import.meta.url)), "../docs/TOOLS.md");

const needsAuth = (description: string) => /Requires IWMM_API_KEY/i.test(description);

function table(rows: { name: string; description: string }[]): string {
  const lines = ["| Tool | Description |", "| --- | --- |"];
  for (const { name, description } of rows) {
    // Trim the auth boilerplate from the cell; it's implied by the section.
    const desc = description.replace(/\s*Requires IWMM_API_KEY\.?\s*$/i, "").trim();
    lines.push(`| \`${name}\` | ${desc.replace(/\|/g, "\\|")} |`);
  }
  return lines.join("\n");
}

const open = tools.filter((t) => !needsAuth(t.description));
const auth = tools.filter((t) => needsAuth(t.description));

const content = `<!-- GENERATED by scripts/gen-tools-doc.ts — do not edit by hand.
     Run \`npm run gen:tools-doc\` after changing tools; CI enforces this. -->
# Tools

${tools.length} tools total. Generated from \`src/tools/index.ts\`.

## No API key required (${open.length})

${table(open)}

## Requires an API key (${auth.length})

Set \`IWMM_API_KEY\` in your MCP client config. Create a key at
<https://iwantmymtg.net/user/api-keys>.

${table(auth)}
`;

if (process.argv.includes("--check")) {
  let current = "";
  try {
    current = readFileSync(OUT, "utf8");
  } catch {
    /* missing file -> stale */
  }
  if (current !== content) {
    console.error(
      "docs/TOOLS.md is out of date. Run `npm run gen:tools-doc` and commit the result.",
    );
    process.exit(1);
  }
  console.log("docs/TOOLS.md is up to date.");
} else {
  writeFileSync(OUT, content);
  console.log(`Wrote ${OUT} (${tools.length} tools).`);
}
